public class Operators {
    public static void main(String[] args) {
        // Arithmetic operators
        int num1 = 50;
        int num2 = 5;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);

        float x = 2.5f;
        float y = 0.6f;
        float z = (x / y);
        System.out.println(z);

        int a = 8;
        int b = 5;
        System.out.println("Modulus result: "+a % b);
    }
}
